#include "ft_ssl.h"

void				ft_zeros_512()
{
	while (g_sh512.b_len % 1024 != 896)
	{
		if (g_sh512.b_reserv == 0)
			ft_add_byte(0, 8);
		g_sh512.b_reserv--;
		g_sh512.b_len++;
	}
	g_sh512.data_len = g_sh512.b_len / 8;
}
